"""
Data loading utilities shared across evaluation and error analysis steps.

Provides gold triplet loading, prediction deserialization, Stanza config
loading, and UD tree parsing for test sets.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any, Dict, List, Optional

import yaml
from tqdm import tqdm

from src.extraction.schema import LegalTriplet, DependencyTree
from src.linguistic.stanza_parser import StanzaParser
from src.utils.logging import get_logger
from src.utils.io import read_jsonl, load_pydantic_list

logger = get_logger(__name__)


def load_predictions(file_path: str) -> List[Dict]:
    """Load predictions from a JSONL file.

    Returns an empty list (with warning) if the file does not exist,
    so that evaluation can continue without crashing on a missing
    experiment output.

    Args:
        file_path:  Path to the predictions JSONL file.

    Returns:
        List of prediction dicts.
    """
    path = Path(file_path)
    if not path.exists():
        logger.warning(
            "Predictions file '%s' not found -- using empty list", file_path,
        )
        return []
    return read_jsonl(str(path))


def load_gold_triplets(file_path: str) -> List[LegalTriplet]:
    """Load gold-standard LegalTriplets from a JSONL file.

    If the file does not exist, prints a prominent warning and returns
    an empty list.  The caller must handle the empty-gold case gracefully.

    Args:
        file_path:  Path to gold triplets JSONL.

    Returns:
        List of LegalTriplet objects, or empty list if file is missing.
    """
    path = Path(file_path)
    if not path.exists():
        logger.warning(
            "=" * 60 + "\n"
            "GOLD TRIPLETS FILE NOT FOUND: %s\n"
            "Gold triplets must be generated by the annotation pipeline\n"
            "(src/annotation/) before evaluation metrics can be computed.\n"
            "The evaluation will produce empty metrics until gold labels exist.\n"
            "=" * 60,
            file_path,
        )
        return []
    return load_pydantic_list(str(path), LegalTriplet)


def load_predictions_as_triplets(predictions: List[Dict]) -> List[LegalTriplet]:
    """Convert prediction dicts (from extract steps) into LegalTriplet objects.

    Each prediction dict has a ``triplet`` key containing the serialized
    LegalTriplet.  Deserializes back into a Pydantic model.

    Args:
        predictions:  List of prediction dicts.

    Returns:
        List of LegalTriplet objects.
    """
    triplets: List[LegalTriplet] = []
    for pred in predictions:
        triplet_data = pred.get("triplet", {})
        if not triplet_data:
            triplet = LegalTriplet()
        else:
            try:
                triplet = LegalTriplet.model_validate(triplet_data)
            except Exception as exc:
                logger.debug(
                    "Failed to validate triplet for clause %s: %s",
                    pred.get("clause_id", "?"), exc,
                )
                triplet = LegalTriplet()
        triplets.append(triplet)
    return triplets


def _load_stanza_config(config_path: str) -> Dict[str, Any]:
    """Load Stanza configuration from model.yaml."""
    config_file = Path(config_path)
    if config_file.exists():
        with open(config_file, "r", encoding="utf-8") as fh:
            config = yaml.safe_load(fh) or {}
            return config.get("stanza", {})
    return {}


def parse_trees_for_testset(
    testset_path: str,
    config_path: str,
    max_clauses: Optional[int] = None,
) -> List[DependencyTree]:
    """Parse UD dependency trees for all clauses in the test set.

    The trees are needed for linguistic metrics and error analysis to
    determine primary error categories (e.g., detecting passive voice,
    condition clauses).

    Args:
        testset_path:  Path to lexspec_100.jsonl.
        config_path:   Path to model.yaml (for Stanza config).
        max_clauses:   Optional cap on number of clauses to parse.

    Returns:
        List of DependencyTree objects, 1:1 with test set clauses.
    """
    testset = read_jsonl(str(testset_path)) if Path(testset_path).exists() else []
    if not testset:
        logger.warning("Test set not found -- no UD trees available.")
        return []

    # Load Stanza config.
    config_file = Path(config_path)
    stanza_cfg: Dict[str, Any] = {}
    if config_file.exists():
        with open(config_file, "r", encoding="utf-8") as fh:
            full_config = yaml.safe_load(fh) or {}
            stanza_cfg = full_config.get("stanza", {})

    parser = StanzaParser(
        lang=stanza_cfg.get("lang", "en"),
        processors=stanza_cfg.get("processors",
                                   "tokenize,mwt,pos,lemma,depparse"),
        download_method=stanza_cfg.get("download_method", "REUSE_RESOURCES"),
    )

    clauses = testset[:max_clauses] if max_clauses else testset
    trees: List[DependencyTree] = []

    logger.info("Parsing UD trees for %d test clauses...", len(clauses))
    for clause in tqdm(clauses, desc="Parsing test clauses", unit="clause"):
        text = clause.get("text", "")
        if text.strip():
            try:
                tree = parser.parse(text)
                trees.append(tree)
            except Exception as exc:
                logger.debug("Stanza parse failed: %s", exc)
                trees.append(DependencyTree(text=text, tokens=[]))
        else:
            trees.append(DependencyTree(text="", tokens=[]))

    return trees
